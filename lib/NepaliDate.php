    // Days in the given BS month (1-12), or null if year/month is out of range.
    public static function daysInMonth(int $bsYear, int $bsMonth): ?int {
        if (!isset(self::$DAYS[$bsYear]) || $bsMonth < 1 || $bsMonth > 12) return null;
        return self::$DAYS[$bsYear][$bsMonth - 1];
    }

    // Converts a Gregorian 'YYYY-MM-DD' date to a BS 'YYYY-MM-DD' string.
    // Returns null if the date falls outside the supported BS 1975-2099 range.
    public static function adToBs(string $adDate): ?string {
        if (!preg_match('/^\d{4}-\d{2}-\d{2}$/', $adDate)) return null;
        try {
            $epoch  = new DateTimeImmutable(self::EPOCH_START_AD, new DateTimeZone('UTC'));
            $target = new DateTimeImmutable($adDate, new DateTimeZone('UTC'));
        } catch (Exception $e) {
            return null;
        }
        $diff = (int)$epoch->diff($target)->format('%r%a');
        if ($diff < 0) return null;

        $remaining = $diff;
        for ($y = self::MIN_YEAR; $y <= self::MAX_YEAR; $y++) {
            $yearTotal = self::$DAYS[$y][12];
            if ($remaining >= $yearTotal) { $remaining -= $yearTotal; continue; }
            for ($m = 0; $m < 12; $m++) {
                $monthDays = self::$DAYS[$y][$m];
                if ($remaining >= $monthDays) { $remaining -= $monthDays; continue; }
                return sprintf('%04d-%02d-%02d', $y, $m + 1, $remaining + 1);
            }
        }
        return null; // beyond MAX_YEAR
    }

    // Converts a BS year/month/day to a Gregorian 'YYYY-MM-DD' string.
    // Returns null if the date is invalid or out of the supported range.
    public static function bsToAd(int $bsYear, int $bsMonth, int $bsDay): ?string {
        $maxDay = self::daysInMonth($bsYear, $bsMonth);
        if ($maxDay === null || $bsDay < 1 || $bsDay > $maxDay) return null;

        $totalDays = 0;
        for ($y = self::MIN_YEAR; $y < $bsYear; $y++) $totalDays += self::$DAYS[$y][12];
        for ($m = 0; $m < $bsMonth - 1; $m++) $totalDays += self::$DAYS[$bsYear][$m];
        $totalDays += $bsDay - 1;

        $epoch  = new DateTimeImmutable(self::EPOCH_START_AD, new DateTimeZone('UTC'));
        $target = $epoch->modify("+{$totalDays} days");
        return $target->format('Y-m-d');
    }
